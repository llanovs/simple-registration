    private static final String EMAIL_NOT_VALID = "Email %s not valid";
    private static final String TOKEN_NOT_FOUND = "Token not found";
    private static final String TOKEN_EXPIRED = "Token expired";
    private static final String EMAIL_ALREADY_CONFIRMED = "Email already confirmed";
    private static final String EMAIL_CONFIRMED = "Email confirmed";

    private final ConfirmationTokenService confirmationTokenService;
    private final EmailValidator emailValidator;
    private final EmailSender emailSender;
    private final UserService userService;

    public String register(RegistrationRequest request) {
        boolean isValidEmail = emailValidator.test(request.getEmail());

        if(!isValidEmail){
            throw  new IllegalArgumentException(String.format(EMAIL_NOT_VALID, request.getEmail()));
        }

        String token = userService.addUser(new UserEntity(
                request.getName(),
                request.getSurname(),
                request.getBirthday(),
                request.getEmail(),
                request.getPassword(),
                AppUserRole.USER));

        String link = "http://localhost:8085/api/v1/registration/confirm?token=" + token;
        emailSender.send(request.getEmail(), buildEmail(request.getName(), link));

        return token;
    }

    @Transactional
    public String confirmedToken(String token){
        ConfirmationToken confirmationToken = confirmationTokenService.
                getToken(token)
                .orElseThrow(() -> new IllegalStateException(TOKEN_NOT_FOUND));

        if (confirmationToken.getConfirmedAt() != null){
            throw new IllegalStateException(EMAIL_ALREADY_CONFIRMED);
        }

        LocalDateTime expiredAt = confirmationToken.getExpiresAt();

        if (expiredAt.isAfter(LocalDateTime.now())){
            throw new IllegalStateException(TOKEN_EXPIRED);
        }

        confirmationTokenService.setConfirmedAt(token);
        userService.enableUser(confirmationToken.getUserEntity().getEmail());

        return EMAIL_CONFIRMED;
    }

